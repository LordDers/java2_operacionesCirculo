public class Circulos 
{

	//Metodo para calcular el area
	public float area (float num1) 
	{
		float resultado = 0;
		resultado = 3.1416f * (num1 * num1);
		return resultado;
	}

	//Metodo para calcular la circunferencia
	public float circunferencia (float num1) 
	{
		float resultado = 0;
		resultado = 2 * 3.1416f * num1;
		return resultado;
	}
}
